                                    8. Mampu menjelaskan proses pengendalian dan 
                                    pemeriksaan tanda izin masuk orang dan 
                                    barang; <br>
                                    9. Mampu menjelaskan proses pengawasan dan 
                                    pemeriksaan orang yang melewati pintu 
                                    masuk ke Bandar Udara; <br>
