                                    pengawasan pintu masuk dan melakukan 
                                    pemeriksaan keamanan terhadap kendaraan; <br>
                                    11. Mampu menjelaskan proses pengendalian 
                                    orang yang masuk ke area penanganan bagasi 
                                    tercatat; <br>
                                    12. Mampu menjelaskan proses perlindungan 
